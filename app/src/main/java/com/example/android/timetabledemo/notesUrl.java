package com.example.android.timetabledemo;

public class notesUrl {

    public String name,url;

    public notesUrl(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public notesUrl()
    {

    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }
}
